#metodos uteis para dicts

# len - Mostra quantas chaves tem
# keys - iteravel com as chaves ( retorna o nome das chaves que existem no dicionario)
# values - iteravel com os valores ( mostra os valores que estao nas chaves)
# item - iteravel com chave e valores ( mostra a chave e o valor)
# setdefault - adiciona valor se a chave nao existe
# copy - retorna uma copia rasa ( shallow copy) ao inves de substituir ele copia uma dict, criando 2 iguais
# get - obtem uma chave 
# pop - apaga um item com uma chave especifica (del)
# popitem - apaga o ultimo item adicionado
# update - atualiza um dicionario, onde vocÃª pode reescrever chaves e items 
# deepcopy - parecido com o copy, mas o programa aprofunda nos listas internas do dicionario