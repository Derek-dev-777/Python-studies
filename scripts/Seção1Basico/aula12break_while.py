# while é uma função de repetição, e o break quebra essa repetição
# Operadores de atribuição
# = , +=, -=, *=, /=, //=, %=, **=
# continue, serve para fazer mudanças no meio do while se for preciso, em um respectivo numero ou sla.

contador  = 0 

while contador < 10:
    print(contador)
    contador = contador + 1

print("acabou")